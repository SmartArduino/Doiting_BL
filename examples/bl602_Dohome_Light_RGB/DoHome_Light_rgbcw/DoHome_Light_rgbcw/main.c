int64_t rtc_start = 0;
TickType_t xtick_s = 0;

void correct_sys_tick()
{
    TickType_t xtick_e, xtick_delta, xtick_jump;
    uint32_t delta_ms = 0;
    int64_t rtc_end;

    taskENTER_CRITICAL();
    rtc_end = hal_timer_now_ms();
    delta_ms = (uint32_t)(rtc_end - rtc_start);
    xtick_e = xTaskGetTickCount();
    xtick_delta = xtick_e - xtick_s;

    if (delta_ms >= xtick_delta){
        xtick_jump = delta_ms - xtick_delta;
    } else {
        xtick_jump = 0;
    }
    vTaskStepTickSafe(xtick_jump);
    taskEXIT_CRITICAL();
    //printf("rtc end:%lld, correct systick-->delta_ms:%ld, xtick_delta:%d, xtick_jump:%d\r\n", rtc_end, delta_ms, xtick_delta, xtick_jump);
}

